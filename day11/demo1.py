def add(p1,p2):
    print("*" * 30)
    print(f"inside add")
    print(f"p1={p1}, p2={p2}")
    addition=p1+p2
    print(f"addition={addition}")
    print("*" * 30)
    
add(10,20)    
    
def mul(p1,p2):
    print("*" * 30)
    print(f"inside mul")
    print(f"p1={p1}, p2={p2}")
    multiply=p1*p2
    print(f"multiplication={multiply}")
    print("*" * 30)
    
mul(20,20)    
        